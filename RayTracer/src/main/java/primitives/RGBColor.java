package primitives;

public class RGBColor {
	
	private byte r;
	private byte g;
	private byte b;
	
	public RGBColor(byte r, byte g, byte b) {
		super();
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
	public byte getR() {
		return r;
	}
	
	public byte getG() {
		return g;
	}
	
	public byte getB() {
		return b;
	}
	
	
}
